    cx[13] = c[39] * x[1] * x[6] + c[40] * x[1] + c[41] * x[3]
    cx[14] = c[42] * x[3] / x[1] + c[43] / x[1] + c[44] * x[6]
    return cx
  end

  x0 = T[1745.0, 110.0, 3048.0, 89.0, 92.0, 8.0, 145.0]
