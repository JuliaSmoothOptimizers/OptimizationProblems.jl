export spmsrtls

function spmsrtls(args...; n::Int = default_nvar, kwargs...)
  m = max(Int(round((n + 2) / 3)), 34)
  n = m * 3 - 2
  p = [sin(i^2) for i = 1:n]
  x0 = [p[i] / 5 for i = 1:n]

  nlp = Model()
  @variable(nlp, x[i = 1:n], start = x0[i])
  @objective(
    nlp,
    Min,
    0.5 * sum(
      (
        x[(3 * (i - 1) + 1) - 4] * x[(3 * (i - 1) + 1) - 1] -
        p[(3 * (i - 1) + 1) - 4] * p[(3 * (i - 1) + 1) - 1]
      )^2 for i = 3:m
    ) +
    0.5 * sum(
      (
        x[(3 * (i - 1) + 1) - 3] * x[(3 * (i - 1) + 1) - 1] +
        x[(3 * (i - 1) + 1) - 1] * x[(3 * (i - 1) + 1)] -
        p[(3 * (i - 1) + 1) - 3] * p[(3 * (i - 1) + 1) - 1] -
        p[(3 * (i - 1) + 1) - 1] * p[(3 * (i - 1) + 1)]
      )^2 for i = 2:m
    ) +
    0.5 * sum((x[(3 * (i - 1) + 1)]^2 - p[(3 * (i - 1) + 1)]^2)^2 for i = 1:m) +
    0.5 * sum(
      (
        x[(3 * (i - 1) + 1) - 2] * x[(3 * (i - 1) + 1) - 1] -
        p[(3 * (i - 1) + 1) - 2] * p[(3 * (i - 1) + 1) - 1]
      )^2 for i = 2:m
    ) +
    0.5 * sum(
      (
        x[(3 * (i - 1) + 1) + 2] * x[(3 * (i - 1) + 1) + 1] -
        p[(3 * (i - 1) + 1) + 2] * p[(3 * (i - 1) + 1) + 1]
      )^2 for i = 1:(m - 1)
    ) +
    0.5 * sum(
      (
        x[(3 * (i - 1) + 1) + 3] * x[(3 * (i - 1) + 1) + 1] +
        x[(3 * (i - 1) + 1) + 1] * x[(3 * (i - 1) + 1)] -
        p[(3 * (i - 1) + 1) + 3] * p[(3 * (i - 1) + 1) + 1] -
        p[(3 * (i - 1) + 1) + 1] * p[(3 * (i - 1) + 1)]
      )^2 for i = 1:(m - 1)
    ) +
    0.5 * sum(
      (
        x[(3 * (i - 1) + 1) + 4] * x[(3 * (i - 1) + 1) + 1] -
        p[(3 * (i - 1) + 1) + 4] * p[(3 * (i - 1) + 1) + 1]
      )^2 for i = 1:(m - 2)
    )
  )
  return nlp
end
